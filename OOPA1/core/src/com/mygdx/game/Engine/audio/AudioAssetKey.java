package com.mygdx.game.Engine.audio;

/**
 * Enumerates keys for identifying specific audio assets within the game.
 * This approach allows for a more organized and readable way of managing
 * audio resources, such as sound effects and background music.
 */
public enum AudioAssetKey {
    // Key for the first background music asset.
    BG_1,
    // Key for the second default audio asset.
    DEFAULT_TWO,
    // Key for the mouse click sound effect asset.
    MOUSE_CLICK,
}
